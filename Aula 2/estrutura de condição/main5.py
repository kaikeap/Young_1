number = int(input('digite um numero de 1 a 7'))

if number == 1:
    print('segunda-feira')

elif number == 2:
    print('terça-feira')

elif number == 3:
    print('quarta-feira')

elif number == 4:
    print('quita-feira')

elif number == 5:
    print('sexta-feira')

elif number == 6:
    print('sábado')

elif number == 7:
    print('domingo')

else:
    print('numero invalido')